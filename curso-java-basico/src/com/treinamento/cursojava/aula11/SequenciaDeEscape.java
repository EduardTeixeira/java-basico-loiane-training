package com.treinamento.cursojava.aula11;

public class SequenciaDeEscape {

	public static void main(String[] args) {

		System.out.println("\t t-Teste de Escape, espaço na primeira linha");

		System.out.println("\b b-Teste de Escape, espaço na primeira linha");

		System.out.println("\n n-Teste de Escape, espaço na primeira linha");

		System.out.println("\r r-Teste de Escape, espaço na primeira linha");

		System.out.println("\f f-Teste de Escape, espaço na primeira linha");

		System.out.println("\' '-Teste de Escape, espaço na primeira linha");

		System.out.println("\" \"Teste de Escape, espaço na primeira linha");

		System.out.println("\\ \\Teste de Escape, espaço na primeira linha");

		System.out.print("\"Hello, World\"\n\r");

	}

}
