package com.treinamento.cursojava.aula11;

public class VariaveisChar {

	public static void main(String[] args) {

		char o = 'o';

		char i = 'i';

		System.out.println(o + i);

		char oNumeral = 111;

		char iNumeral = 105;

		char interrogacaoNumeral = 0x3f;

		System.out.println("" + oNumeral + iNumeral + interrogacaoNumeral);

	}

}
