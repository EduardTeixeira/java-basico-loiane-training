package com.treinamento.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {

		int valor1 = 1;
		int valor2 = 2;

		System.out.println(valor1 == valor2);

		System.out.println(valor1 != valor2);

		System.out.println(valor1 >= valor2);

		System.out.println(valor1 <= valor2);

		System.out.println(valor1 > valor2);

		System.out.println(valor1 < valor2);

	}

}
